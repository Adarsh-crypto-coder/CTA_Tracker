package com.example.ctatracker;

public class Route {
    private String rtnm;
    private String rtclr;
    private String rtdd;
    private String rt;

    public void setRtnm(String rtnm) {
        this.rtnm = rtnm;
    }

    public void setRt(String rt) {
        this.rt = rt;
    }

    public void setRtdd(String rtdd) {
        this.rtdd = rtdd;
    }

    public void setRtclr(String rtclr) {
        this.rtclr = rtclr;
    }

    public String getRtnm() {
        return rtnm;
    }

    public String getRt() {
        return rt;
    }

    public String getRtclr() {
        return rtclr;
    }

    public String getRtdd() {
        return rtdd;
    }


}
